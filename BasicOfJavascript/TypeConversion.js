// Strings to Numbers
let y = "5";
let x = Number(y);
console.log(typeof x);

// Dates to Numbers
d = new Date();
console.log(d.getDate());
console.log(Number(d));

// Dates to String
console.log(Date().toString())

// Booleans to Number
console.log(Number(false))

// Booleans to Strings
let a = false.toString();
console.log(typeof a);

let o = 8;
let p = o.toString();
console.log(typeof p);


